	<h3><a href="#">What can Nowcorkit do for me?</a></h3>
	<div>
		<p>
		Nowcorkit offers conveniences for both types of users, Board Hosts and Board Posters.
		Those who host a digital corkboard, using any web compatible device of their choosing, can
		set a feature that we included called, "Pay-Per-Space" (PPS). PPS allows the board host to reserve a
		maximum of FOUR flyer slots. This in turn, allows flyers to stay visible (not covered by new flyers) 
		for a specific time period that the user paid for. 
		</p>
		
		<p>
		But why the flyer-slot cap? We placed a cap on the maximum amount of reserved slots to continue keeping the
		the grass roots reason for the corkboard: free. Thus, PPS gives an alternative for both parties to embrace
		a new age of technology.
		</p>
		
		<p>
		For Board Posters, we provide the ability of trans-united posting. Meaning, if there is a flyer that you wish
		to post to in another state, the user will have full ability to do so. Flyer creation also comes with a custom
		QRcode that provides a link to the the flyer itself. This way, if someone wants to save the flyer, they simply will need
		to scan the QRcode to retrieve its information. Forget paper tabs!
		</p>
