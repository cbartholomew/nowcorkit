		<h3>Create Board</h3>
		<p>
			Nowcorkit.com allows people to create their own boards to display too. To create a board, simply click on "Board Manager". From the drop down menu, select "Create Board"
			and fill in the required information. When you are done with filling in the required information, you can press the "Create" button towards the bottom right. 
			If you want more information about the board features, such as Permissions, Flyer Expiration Days, and Pay Per Space Features, please review "Board Features" below.
		</p>
		<p>
			After creating your board, it will be listed in the drop down box. To manage your board, select it from the drop down menu.
		</p>
		<h3>Manage Board</h3>
		<p>
			Once you have chosen the specific board from the drop down menu, the following menu will appear below with the following tabs
		</p>	
		<ul>
			<li>Instructions - This tab gives you the option to Display your board, and it will give you the option to remove your board. For information on Display Board, 
				please review "Displaying Board" help below.</li>
			<br>
			<li>General - Here, you have the option to modify the settings of your board such as title, description, address, city, and state.</li>
			<br>
			<li>Permission - In this tab, you will have the ability to modify the permission of your board. This includes Approval, Private, and Public. See Board Features help
				for more information about approval statuses</li>
			<br>
			<li>Posting - In this tab, users will be able to set an automatic expiration day for flyers on their board. This helps recycle posts that are old. 
				Other options here include Pay Per Space management, which allocates four spots for the board host to accept a payment on. This allows flyers to stay 
				visible (not covered) for a timeframe specified by the board host</li>
			<br>
			<li>Posts - This tab allows an interactive way for users to manage flyers that are sent to their corkboard. In this tab, board hosts are able to preview, remove, approve, 
				and start a PPS campaign on any of the flyers. This menu also provides a filter option for the users to quickly find, which type of posts they are looking for.</li>
		</ul>
