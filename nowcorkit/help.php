	<h3>Create Flyers</h3>
	<p>Creating flyers are an integral part of corkboards. Nowcorkit.com allows users to create three different flyer types:</p>		
	<ul>
		<li><i>Text Only: Post a flyer using a template, which includes a title, description, location, and contact information. This is text only with QRCode capability.</i></li>
		<br>
		<li><i>Text & Image: Post a flyer using a template, which includes the same fields as text, and the ability to upload a smaller image with QRCode capability.</i></li>
		<br>
		<li><i>Image: Have a flyer already made? Use it! Upload a full flyer. No additional text fields are available through this option. QRCode capability is not enabled.</i></li>
	</ul>
	<p>To start creating flyers, click on "Create Flyer" on the left side of the menu. Choose your template type, and fill in the required fields!</p>
	
	<h3>Manage Flyers</h3>
	<p>To preview, edit, or remove features about your flyer. Click the "Flyer Manager" button on the left side. This menu separates your flyers in three different categories text, text & image, and image. You can interact with your flyers in three different ways:</p> 
	<ul>
		<li><i>Preview: Allows the user to preview the flyer they have just created.</i></li>
		<br>
		<li><i>Edit: Allows the user to edit the <b>text</b> of the flyer. For Image types, this includes the title only.</i></li>
		<br>
		<li><i>Remove: This will permanently delete the flyer from your account and remove its presence from all boards. This is not the same as removing a post!</i></li>
	</ul>
	<h3>Post Flyers</h3>
	<p>Now that you have created your flyer, you should now-cork-it! (Post it!) Corking a flyer can be done in three steps:</p>
	<ol>
		<li>View Boards' Locations by State</li> 
		<p>
		   You will be defaulted to the state you signed up with. However, this doesn't limit you to post in other states. 
		   The idea is that you first choose a state then after you choose a state, you can look at a list of locations in that state. 
		   After you choose a location, google maps will display address information about the location that you have chosen. The 
		   locations are the actual "corkboards" that are open for posting.
		</p>
		<li>Choose a Location to Post a Flyer</li>
		<p>After you have determined a state that you are going to post to. You'll have to choose a location. Each location has three distinct permissions:</p>
		<ul>
		<li>Public - Anyone can post to this board with no wait time. The post will show up immediately.</li>
		<br>
		<li>By Approval - The corkbord host must first approve your flyer in their board's post control. Once completed, the flyer will post immediately.</li>
		<br>
		<li>Private - Private means that only the corkboard host can post to this board. This is used mostly for organizations.</li>
		</ul>		
		<p>
			If the board host chooses to do so, the board host may enable Pay Per Service feature (PPS). More information about PPS can be found in Boards Help; however,
			the idea of PPS is that each board host will be reserved four slots that they can charge for. These slots ensure that the posters flyer stays visible for the 
			set amount of time that was promised by the board owner. PPS Info allows the poster to view information that the board host has set, such as how to deliver payment.
		</p>
		<p>
			After reviewing the PPS information, you can then choose the flyer that you created to post. Simply choose a flyer from the Flyer drop down list, and then simply choose the 			
			"plus" icon, which means you are "adding" the flyer to this location's board. 
		</p>
		<li>Step 3 - View Flyers' Status or Remove Your Posts'</li>
		<p>
		   Once your flyer has been posted from step two, your flyer will show up on the table. Here, you can review its status, expiration date (which is determined in the board 
		   hosts settings), and you are then given the option to remove your flyer from this location. There are four statuses that your flyer can be in at anytime:
		</p>
		<ul>
			<li>Posted - Your flyer has been posted on the board, and it is currently viewable</li>
			<br>
			<li>Pending Approval - Your flyer has been posted to a board that requires the board owner's approval. Once approved, it will show as "Posted"</li>
			<br>
			<li>Not Approved - Your flyer was not approved, and your post has been removed from the board owner's queue.</li>
			<br>
			<li>PPS Posted - Your flyer's PPS campaign has started. It will set itself back to Posted status when the PPS expiration date is met.</li>			
		</ul>
		<p>
			At anytime you wish to remove a flyer from the location's corkboard, you may do so by simple pressing the "Remove" (minus sign) button.
		</p>
